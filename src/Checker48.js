function generateLiveCard(freq) {
  const COLS = ["B","I","N","G","O"];
  const needed = { B:5, I:5, N:4, G:5, O:5 };
  const card = {};
  for (const col of COLS) {
    const [lo, hi] = COL_RANGES[col];
    const pool = [];
    for (let n = lo; n <= hi; n++) pool.push({ n, f: freq[n]||0 });
    pool.sort((a,b) => b.f - a.f || Math.random() - 0.5);
    let nums = pool.slice(0, needed[col]).map(b => b.n);
    nums = shuffleArr(nums);
    card[col] = nums;
    if (col === "N") card[col].splice(2, 0, "FREE");
  }
  return card;
}

