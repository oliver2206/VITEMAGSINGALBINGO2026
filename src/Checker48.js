function BingoCardSingle({ card, freq, cardKey, pattern, simDrawnSet, simWinLines }) {
  const COLS = ["B","I","N","G","O"];
  const patternSet = new Set();
  if (pattern) {
    if (pattern.cells === "all") { COLS.forEach((_,ci)=>{for(let r=0;r<5;r++)patternSet.add(`${ci},${r}`);}); }
    else if (pattern.cells === null) { [[0,0],[1,1],[2,2],[3,3],[4,4]].forEach(([c,r])=>patternSet.add(`${c},${r}`)); }
    else { pattern.cells.forEach(([c,r])=>patternSet.add(`${c},${r}`)); }
  }

  const freqBadgeStyle = getFreqBadgeStyle;

  const isSim = !!simDrawnSet;

  return (
    <div className="bingo-card-grid" key={cardKey}>
      {COLS.map((col) => (
        <div key={col} className="bc-col-header" style={{ background:`${COL_COLORS[col]}20`, color:COL_COLORS[col] }}>{col}</div>
      ))}
      {[0,1,2,3,4].map(row =>
        COLS.map((col, ci) => {
          const val = card[col][row];
          const isFree = val === "FREE";
          const f = isFree ? 0 : (freq[val] || 0);
          const c = isFree ? "#C8A85A" : COL_COLORS[col];
          const isPatternCell = patternSet.has(`${ci},${row}`);
          const patternColor = pattern ? pattern.color : "#C8A85A";
          const bs = !isFree ? freqBadgeStyle(f) : null;

          // Simulation states
          const isDrawn = isSim && !isFree && simDrawnSet.has(val);
          const inWin   = isSim && cellInWin(ci, row, simWinLines) && (isFree || isDrawn);

          let cls = isFree ? "bc-cell free" : f >= 4 ? "bc-cell hot4" : f === 3 ? "bc-cell hot3" : f === 2 ? "bc-cell hot2" : "bc-cell hot1";

          let extraBg = "";
          let extraBorder = isPatternCell ? patternColor+"70" : `${c}20`;
          let extraShadow = isPatternCell ? `inset 0 0 0 2px ${patternColor}40` : "none";

          if (isSim) {
            if (inWin)   { extraBg = `${c}35`; extraBorder = `${c}80`; extraShadow = `inset 0 0 0 2px ${c}55, 0 0 10px ${c}30`; }
            else if (isDrawn) { extraBg = `${c}18`; extraBorder = `${c}45`; }
          }

          return (
            <div key={`${col}${row}`}
              className={`${cls}${isPatternCell?" pattern-cell":""}${inWin?" sim-win-cell":""}`}
              style={{
                color: isFree ? "#C8A85A" : c,
                borderColor: extraBorder,
                boxShadow: extraShadow,
                ...(extraBg ? { background: extraBg } : {}),
              }}>
              <span style={{ fontSize:"clamp(15px,2.2vw,22px)", fontWeight:700, lineHeight:1 }}>
                {isFree ? "★" : val}
              </span>
              {!isFree && (
                <span className="bc-freq-badge" style={{ ...bs }}>
                  {f}×
                </span>
              )}
              {isSim && isDrawn && !isFree && (
                <span style={{ position:"absolute", top:3, right:4, fontSize:9, fontWeight:700, color:c }}>✓</span>
              )}
              {isPatternCell && !isFree && !isSim && (
                <span className="bc-pattern-badge" style={{ color: patternColor }}>★</span>
              )}
            </div>
          );
        })
      )}
    </div>
  );
}

